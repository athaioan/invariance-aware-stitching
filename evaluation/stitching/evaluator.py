@torch.no_grad()
def eval(model, model_reference, clf_loss, data_loader, epoch, args, verbose=True):

    metric_logger = MetricLogger(delimiter="  ")
    header = "Epoch: [{}/{}]".format(epoch, args.epochs)

    model.eval()
    model_reference.eval()

    for _, (image, label) in enumerate(metric_logger.log_every(data_loader, 10, header, verbose=verbose)):

        image = image.cuda(non_blocking=True)
        label = label.cuda(non_blocking=True)

        # compute end model output
        end_output = model_reference(image)

        # compute stitched model output
        output = model(image)

        loss = clf_loss(output, label)
        acc1, acc5 = accuracy(output, label, topk=(1, 5))
        acc1_end,  = accuracy(end_output, label)

        soft_acc1  = soft_accuracy(output, label)
        soft_acc1_end  = soft_accuracy(end_output, label)

        hard_agreement, soft_agreement = agreement(output, end_output, label)

        batch_count = label.size(0)

        # record logs
        metric_logger.update(loss=loss.item(), n=batch_count)
        metric_logger.update(top1=acc1.item(), n=batch_count)
        metric_logger.update(top5=acc5.item(), n=batch_count)
        metric_logger.update(soft_top1=soft_acc1.item(), n=batch_count)
        metric_logger.update(top1_end=acc1_end.item(), n=batch_count)
        metric_logger.update(soft_top1_end=soft_acc1_end.item(), n=batch_count)

        if hard_agreement is not None:
            metric_logger.update(agreement_ratio=hard_agreement, n=batch_count)

        if soft_agreement is not None:
            metric_logger.update(agreement_prob=soft_agreement, n=batch_count)

    metric_logger.synchronize_between_processes()

    if hard_agreement is not None:
        hard_k_consistency = hard_error_consistency(metric_logger.meters['agreement_ratio'].global_avg / 100.0,
                                                     metric_logger.meters['top1'].global_avg / 100.0,
                                                     metric_logger.meters['top1_end'].global_avg / 100.0,)
        metric_logger.update(hard_k_consistency=hard_k_consistency)

        
    if soft_agreement is not None:
        soft_k_consistency = soft_error_consistency(metric_logger.meters['agreement_prob'].global_avg / 100.0,
                                                     metric_logger.meters['soft_top1'].global_avg / 100.0,
                                                     metric_logger.meters['soft_top1_end'].global_avg / 100.0,
                                                     num_classes=args.num_classes_end,)

        metric_logger.update(soft_k_consistency=soft_k_consistency)

   
    metric_dict = {f"{k}": meter.global_avg for k, meter in metric_logger.meters.items() if k in ['loss', 'top1', 'top5', 
                                                                                                 'agreement_ratio', 'agreement_prob', 
                                                                                                 'hard_k_consistency', 'soft_k_consistency']}
    if verbose:
        print(f"Averaged stats:", metric_logger, '\n')

    return metric_dict

def eval_robustness(model_trainer,
                    model_reference,
                    clf_loss,
                    data_loader,
                    epoch,
                    args,
                    num_splits=15):
    
    """Evaluates the accuracy of a model on adversarial test examples"""

    def _process_chunk(adv_metrics, model_trainer, model_reference, clf_loss, attack, 
                       clean_val_x, clean_val_y, weight_factor, args):


        def get_attack_dataloader(attack, image, label, args):
            """
            Returns a DataLoader for the adversarial examples generated by the attack.
            """

            print(f"Generating adversarial examples using {attack.__class__.__name__}...")
            # Generate adversarial examples
            image_adv = attack.generate(image, label)

            # Create a DataLoader for the adversarial examples
            adv_data_loader = torch.utils.data.DataLoader(list(zip(image_adv, label)),
                                                        batch_size=args.batch_size_per_gpu,
                                                        num_workers=args.num_workers,
                                                        shuffle=False,
                                                        drop_last=False,
                                                        pin_memory=True)
            return adv_data_loader


        for atk_name, attack in attacks.items():
            attack.set_params()

           
            attack_data_loader = get_attack_dataloader(attack, clean_val_x, clean_val_y, args)

            if atk_name not in adv_metrics:


                adv_metrics[atk_name] = eval(model_trainer, model_reference, clf_loss, attack_data_loader, epoch, args, verbose=False)

                for metric_name in adv_metrics[atk_name].keys():
                    adv_metrics[atk_name][metric_name] *= weight_factor


            else:
                _temp_metrics =  eval(model_trainer, model_reference, clf_loss, attack_data_loader, epoch, args, verbose=False)

                for metric_name in adv_metrics[atk_name].keys():
                    adv_metrics[atk_name][metric_name] += weight_factor * _temp_metrics[metric_name]

        return adv_metrics


    # Model setup
    n_classes, n_channels = len(np.unique(data_loader.dataset.targets)), 3
        
    model = PyTorchClassifier(model=unwrap(model_trainer),
                              loss=clf_loss,
                              input_shape=(n_channels, args.data_height, args.data_width),
                              nb_classes=n_classes,
                              clip_values=(np.array(args.adv_min).reshape(1, 3, 1, 1), 
                                           np.array(args.adv_max).reshape(1, 3, 1, 1)))
                            #   clip_values=(0.0, 1.0))

    # Attacks setup
    attacks = get_attacks(model, args.dataset)

    # Evaluate model on attacks
    adv_metrics = {}
    
    total_count = len(data_loader.dataset)

    split_condition = total_count // num_splits

    id_split = 0 
    count = 0
    weight_factor = []
    clean_val_x = []
    clean_val_y = []

    for _, (image, label) in enumerate(data_loader):

        batch_size = image.size(0)
        count += batch_size

        clean_val_x.append(image.numpy())
        clean_val_y.append(label.numpy())


        if count >= split_condition:


            clean_val_x = np.concatenate(clean_val_x, axis=0)
            clean_val_y = np.concatenate(clean_val_y)

            weight_factor = count/total_count

            print(f"Processing chunk {id_split+1} with {count} samples...", end='\r')
            adv_metrics = _process_chunk(adv_metrics, model_trainer, model_reference, 
                                         clf_loss, attacks, clean_val_x, clean_val_y, weight_factor, args)
            
            ## reset
            id_split += 1
            count = 0
            clean_val_x = []
            clean_val_y = []

    if len(clean_val_x) > 0:

        clean_val_x = np.concatenate(clean_val_x, axis=0)
        clean_val_y = np.concatenate(clean_val_y)

        weight_factor = count/total_count
        print(f"Processing chunk {id_split+1} with {count} samples...", end='\r')
        adv_metrics = _process_chunk(adv_metrics, model_trainer, model_reference, 
                                     clf_loss, attacks, clean_val_x, clean_val_y, weight_factor, args)
        
        
    robust_metrics = {k: 0 for k in next(iter(adv_metrics.values()))}

    for _, adv_stats in adv_metrics.items():
        for metric_name, value in adv_stats.items():
            robust_metrics[metric_name] += value


    robust_metrics = {k: round(v / len(attacks), 2) for k, v in robust_metrics.items()}

    print(f"Averaged stats:", robust_metrics, '\n')

    return robust_metrics

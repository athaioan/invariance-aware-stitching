@torch.no_grad()
def eval(model, clf_loss, data_loader, epoch, args, verbose=True):
    metric_logger = MetricLogger(delimiter="  ")
    header = "Epoch: [{}/{}]".format(epoch, args.epochs)

    model.eval()

    for it, (image, label) in enumerate(
        metric_logger.log_every(data_loader, 10, header, verbose=verbose)
    ):

        image = image.cuda(non_blocking=True)
        label = label.cuda(non_blocking=True)

        # compute output
        output = model(image)

        loss = clf_loss(output, label)
        acc1, acc5 = accuracy(output, label, topk=(1, 5))

        batch_count = label.size(0)


        # record logs
        metric_logger.update(loss=loss.item(), n=batch_count)
        metric_logger.update(top1=acc1.item(), n=batch_count)
        metric_logger.update(top5=acc5.item(), n=batch_count)


    metric_logger.synchronize_between_processes()
    metric_dict = {f"test/{k}": meter.global_avg for k, meter in metric_logger.meters.items()}

    metric_dict = {f"{k}": meter.global_avg for k, meter in metric_logger.meters.items() if k in ['loss', 'top1', 'top5',]}

    if verbose:
        print("Averaged test stats:", metric_logger)
    
    return metric_dict



def eval_robustness(model_trainer,
                    clf_loss,
                    data_loader,
                    epoch,
                    args,):
    

    def _get_attack_dataloader(attack, image, label, args):
        """
        Returns a DataLoader for the adversarial examples generated by the attack.
        """

        print(f"Generating adversarial examples using {attack.__class__.__name__}...")
        # Generate adversarial examples
        image_adv = attack.generate(image, label)

        # Create a DataLoader for the adversarial examples
        adv_data_loader = torch.utils.data.DataLoader(list(zip(image_adv, label)),
                                                      batch_size=args.batch_size_per_gpu,
                                                      num_workers=args.num_workers,
                                                      shuffle=False,
                                                      drop_last=False,
                                                      pin_memory=True)
        return adv_data_loader


    """Evaluates the accuracy of a model on adversarial test examples"""
    # Model setup
    # n_classes, n_channels = get_n_classes_and_channels(str_dataset)

    n_classes, n_channels = len(np.unique(data_loader.dataset.targets)), 3

    model = PyTorchClassifier(model=unwrap(model_trainer),
                              loss=clf_loss,
                              input_shape=(n_channels, args.data_height, args.data_width),
                              nb_classes=n_classes,
                              clip_values=(np.array(args.adv_min).reshape(1, 3, 1, 1), 
                                          np.array(args.adv_max).reshape(1, 3, 1, 1)))
                            #   clip_values=(0.0, 1.0))



    # Dataset setup
    clean_val_x = []
    clean_val_y = []

    for _, (image, label) in tqdm(enumerate(data_loader)):

        clean_val_x.append(image.numpy())
        clean_val_y.append(label.numpy())

    clean_val_x = np.concatenate(clean_val_x, axis=0)
    clean_val_y = np.concatenate(clean_val_y)

    # Attacks setup
    attacks = get_attacks(model, args.dataset)

    # Evaluate model on attacks
    adv_metrics = {}

    for atk_name, attack in attacks.items():

        attack.set_params()
        ## get attack data loader
        attack_data_loader = _get_attack_dataloader(attack, clean_val_x, clean_val_y, args)
      
        adv_metrics[atk_name] = eval(model_trainer, clf_loss, attack_data_loader, epoch, args)
   
    robust_metrics = {k: 0 for k in adv_metrics[atk_name].keys()}

    for atk_name, adv_stats in adv_metrics.items():
        for metric_name, value in adv_stats.items():
            robust_metrics[metric_name] += value 

    robust_metrics = {k: v / len(attacks) for k, v in robust_metrics.items()}

    return robust_metrics
